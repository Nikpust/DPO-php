<?php
    # Регулярное выражение: числа, заключенные в одинарные кавычки
    $pattern = "/'(-?\d+)'/";

    # Получаем входную строку от пользователя
    $string = readline("Введите строку: ");

    # Применяем функцию замены ко всем найденным совпадениям
    echo preg_replace_callback($pattern, "replace", $string);

    # Callback-функция замены: удваивает найденное число и возвращает его снова в кавычках
    function replace($number) {
        return "'" . $number[1]*2 . "'";
    }
?>